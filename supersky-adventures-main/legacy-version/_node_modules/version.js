// version.js

// Define the version number and its properties
const versionInfo = {
  number: "2.0.0 (Final Release)",
  position: {
    bottom: "10px",
    right: "10px",
  },
  color: "#666", // Adjust color as needed
};

// Function to display the version number
function displayVersion(ctx, canvas) {
  ctx.font = `14px Arial`;
  ctx.fillStyle = versionInfo.color;
  ctx.fillText(
    "Version: " + versionInfo.number,
    canvas.width - 200,
    canvas.height - 10
  );
}

// Export the version module function
export { displayVersion };
