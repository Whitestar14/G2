// ui.js

// Function to display the high score
function displayHighScore(ctx, highScore) {
  ctx.font = `20px Enriqueta`;
  ctx.fillStyle = "black";
  ctx.fillText("High Score: " + highScore, 10, 30);
}

// Function to display the player's score
function displayScore(ctx, playerScore) {
  ctx.font = `20px Enriqueta`;
  ctx.fillStyle = "black";
  ctx.fillText("Score: " + playerScore, 10, 60);
}

// Function to display the game over modal
function displayGameOverModal(modalBackdrop, finalScore) {
  // Display game over modal
  modalBackdrop.style.display = "block";

  // Display final score
  const finalScoreText = document.getElementById("finalScore");
  finalScoreText.textContent = finalScore;
}

// Function to hide the game over modal
function hideGameOverModal(modalBackdrop) {
  modalBackdrop.style.display = "none";
}

// Export the UI module functions
export {
  displayHighScore,
  displayScore,
  displayGameOverModal,
  hideGameOverModal,
};
