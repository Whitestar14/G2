// gameLoop.js

// Main game loop function
function gameLoop(update, render) {
  function loop() {
    // Update game state
    update();

    // Render game elements
    render();

    // Request next frame
    requestAnimationFrame(loop);
  }

  // Start the game loop
  loop();
}

// Export the game loop module function
export { gameLoop };
