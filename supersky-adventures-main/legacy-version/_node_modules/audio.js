// audio.js

// Create an audio object for background music
const audio = new Audio("gamemusic.mp3");
audio.loop = true; // Enable loop for continuous playback
audio.volume = 0.5; // Adjust volume as needed

// Function to play audio
function playAudio() {
  audio.play();
}

// Function to pause audio
function pauseAudio() {
  audio.pause();
}

// Export the audio module functions
export { playAudio, pauseAudio };
