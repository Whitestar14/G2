// Function to handle keyboard controls
function handleKeyboardControls(player) {
  document.addEventListener("keydown", function (event) {
    if (event.code === "Space" && !player.jumping) {
      player.jumping = true;
      player.jumpVelocity = player.jumpHeight / 10;
    }
  });
}

// Export the game controls module function
export { handleKeyboardControls };
